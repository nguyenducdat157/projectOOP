private void setTableData(ArrayList<Patient> listPatient, ArrayList<ThaiKi> listThaiKi){
    ArrayList<Patient> listPatientByThaiKi = new ArrayList<>();
        for (int i = 0; i < listThaiKi.size(); i++) {
            for (int j = 0; j < listPatient.size(); j++) {
                if(listPatient.get(j).getID()==listThaiKi.get(i).getIDPatient()){
                    listPatientByThaiKi.add(listPatient.get(j));
                }
                
            }
        }
        
        tblModel = (DefaultTableModel) jTableThaiKi.getModel();
        for(int i = 0;i< listThaiKi.size();  i++) {
                    tblModel.addRow(new Object[]{listThaiKi.get(i).getIDPatient(), listPatientByThaiKi.get(i).getName(), listThaiKi.get(i).getThoiGianMangThai(),
                        listThaiKi.get(i).getWeight(), listThaiKi.get(i).getNearestExaminationDate(),
                        listThaiKi.get(i).getReExaminationDate()

                    });
                }
}
