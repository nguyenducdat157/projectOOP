    private void jbuttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonSearchActionPerformed
        ArrayList<Patient> list = new ArrayList<>();
        String name  = jTextFieldSearch.getText().trim();
        list = new ClinicDAO().getPatientByName(name);
        ArrayList<ThaiKi> listThaiKi = new ArrayList<>();
        ArrayList<Patient> listMom = new ArrayList<>();
        for (Patient patient : list) {
            if (patient.getKindOfPatient().equals("Mẹ mang thai")) {
                listMom.add(patient);
            }
        }
        for (int i = 0; i < listMom.size(); i++) {
            listThaiKi.add(new ClinicDAO().getThaiKiByID(listMom.get(i).getID()));
        }
        if (listMom.size()<1) {
            JOptionPane.showMessageDialog(rootPane, "Không có bệnh nhân thỏa mãn!");
        }
        else{
            tblModel = (DefaultTableModel) jTableThaiKi.getModel();
            tblModel.setRowCount(0);
            for(int i = 0;i< listMom.size();  i++) {
                tblModel.addRow(new Object[]{listMom.get(i).getID(), listMom.get(i).getName(),
                    listThaiKi.get(i).getThoiGianMangThai(),
                    listThaiKi.get(i).getWeight(), listThaiKi.get(i).getNearestExaminationDate(),
                    listThaiKi.get(i).getReExaminationDate()

                });
            }
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jbuttonSearchActionPerformed

