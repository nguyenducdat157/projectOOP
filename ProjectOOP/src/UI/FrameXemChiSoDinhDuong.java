    private void jbuttonSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbuttonSearchActionPerformed
        ArrayList<Patient> list = new ArrayList<>();
        String name  = jTextFieldSearch.getText().trim();
        list = new ClinicDAO().getPatientByName(name);
        ArrayList<ChiSoDinhDuong> listCSDD = new ArrayList<>();
        ArrayList<Patient> listChildren = new ArrayList<>();
        for (Patient patient : list) {
            if (patient.getKindOfPatient().equals("Trẻ em")) {
                listChildren.add(patient);
            }
        }
        for (int i = 0; i < listChildren.size(); i++) {
            listCSDD.add(new ClinicDAO().getChiSoDinhDuongByID(listChildren.get(i).getID()));
        }
        if (listChildren.size()<1) {
            JOptionPane.showMessageDialog(rootPane, "Không có bệnh nhân thỏa mãn!");
        }
        else{
            tblModel = (DefaultTableModel) jTableCSDD.getModel();
            tblModel.setRowCount(0);
            for(int i = 0;i< listCSDD.size();  i++) {
                    tblModel.addRow(new Object[]{listCSDD.get(i).getIDPatient(), listChildren.get(i).getName(),
                        listCSDD.get(i).getMinHeight(),listCSDD.get(i).getHeight(),
                        listCSDD.get(i).getMaxHeight(),
                        listCSDD.get(i).getMinWeight(),
                        listCSDD.get(i).getWeight(),
                        listCSDD.get(i).getMaxWeight()
                        

                    });
                }
            
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_jbuttonSearchActionPerformed

