private void setTableData(ArrayList<Patient> listPatient, ArrayList<ChiSoDinhDuong> listCSDD){
    ArrayList<Patient> listPatientByCSDD = new ArrayList<>();
        for (int i = 0; i < listCSDD.size(); i++) {
            for (int j = 0; j < listPatient.size(); j++) {
                if(listPatient.get(j).getID()==listCSDD.get(i).getIDPatient()){
                    listPatientByCSDD.add(listPatient.get(j));
                }
                
            }
        }
        
        tblModel = (DefaultTableModel) jTableCSDD.getModel();
        for(int i = 0;i< listCSDD.size();  i++) {
                    tblModel.addRow(new Object[]{listCSDD.get(i).getIDPatient(), listPatientByCSDD.get(i).getName(),
                        listCSDD.get(i).getMinHeight(),listCSDD.get(i).getHeight(),
                        listCSDD.get(i).getMaxHeight(),
                        listCSDD.get(i).getMinWeight(),
                        listCSDD.get(i).getWeight(),
                        listCSDD.get(i).getMaxWeight()
                        

                    });
                }
}

